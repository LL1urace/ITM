# 6. Даны три целых числа: A, B, C. Проверить истинность высказывания: «Справедливо двойное неравенство A < B < C».
from typing import Union

def check_double_inequalities(num1: Union[int, float],
                              num2: Union[int, float],
                              num3: Union[int, float]) -> bool:
    try:
        a = int(num1)
        b = int(num2)
        c = int(num3)
    except TypeError:
        raise TypeError("Значения не являются целыми числами!")
    except ValueError:
        raise ValueError("Значения не могут быть преобразованы в числа (значения)!")
    return a < b < c


