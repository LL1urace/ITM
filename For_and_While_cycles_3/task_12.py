# 12. Вывести значения функции y=x^2 от 1 до 10 с шагом 0.5.
def output_func() -> None:
    """Вывести значения функции y=x^2 от 1 до 10 с шагом 0.5."""
    x = 1
    while x <= 10:
        print(f"Значение функции y=x^2 при x = {x} равно: {x ** 2}")
        x += 0.5



if __name__ == "__main__":
    output_func()